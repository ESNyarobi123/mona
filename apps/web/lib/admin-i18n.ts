export type AdminMessageKey = keyof typeof ADMIN_MESSAGES;

export function ta(locale: AppLocale, key: AdminMessageKey): string {
  return ADMIN_MESSAGES[key][locale];
}

export function tf(
  locale: AppLocale,
  key: AdminMessageKey,
  vars: Record<string, string | number>
): string {
  let text = ta(locale, key);
  for (const [k, v] of Object.entries(vars)) {
    text = text.replace(`{${k}}`, String(v));
  }
  return text;
}

